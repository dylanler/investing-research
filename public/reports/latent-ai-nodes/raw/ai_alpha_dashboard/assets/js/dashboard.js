function svgEl(tag, attrs={}, text=''){
  const el = document.createElementNS('http://www.w3.org/2000/svg', tag);
  for(const [k,v] of Object.entries(attrs)) el.setAttribute(k, v);
  if(text) el.textContent = text;
  return el;
}
function renderBarChart(){
  const el = $('#barChart'); el.innerHTML='';
  const W=900,H=420, m={l:180,r:45,t:20,b:30};
  const svg = svgEl('svg',{viewBox:`0 0 ${W} ${H}`, role:'img', 'aria-label':'Top alpha score bar chart'}); el.appendChild(svg);
  const rows = [...DATA].sort((a,b)=>b.alpha_score-a.alpha_score).slice(0,22);
  const xMax = Math.max(...rows.map(d=>d.alpha_score));
  const bw = (H-m.t-m.b)/rows.length - 4;
  rows.forEach((d,i)=>{
    const y=m.t+i*((H-m.t-m.b)/rows.length);
    const w=(d.alpha_score/xMax)*(W-m.l-m.r);
    const rect=svgEl('rect',{x:m.l,y:y,width:w,height:bw,rx:6,fill:d.region==='US'?'#334155':'#64748b'});
    rect.addEventListener('mousemove', e=>showTip(e, d)); rect.addEventListener('mouseleave', hideTip); svg.appendChild(rect);
    svg.appendChild(svgEl('text',{x:m.l-10,y:y+bw*.68,'text-anchor':'end',class:'bar-label'},`${d.region_rank}. ${d.ticker} ${d.company.slice(0,22)}`));
    svg.appendChild(svgEl('text',{x:m.l+w+6,y:y+bw*.68,class:'bar-label'},d.alpha_score));
  });
  svg.appendChild(svgEl('line',{x1:m.l,y1:H-m.b,x2:W-m.r,y2:H-m.b,class:'axis'}));
  for(let t=40;t<=90;t+=10){
    const x=m.l+(t/xMax)*(W-m.l-m.r); svg.appendChild(svgEl('line',{x1:x,y1:H-m.b,x2:x,y2:H-m.b+5,class:'axis'})); svg.appendChild(svgEl('text',{x:x,y:H-8,'text-anchor':'middle',class:'axis-text'},t));
  }
}
