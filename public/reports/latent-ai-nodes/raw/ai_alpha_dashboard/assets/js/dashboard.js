function scoreClass(s){ return s>=75?'high':(s>=60?'med':'low'); }
function fmt(n){ return Number(n).toFixed(0); }
function unique(arr){ return [...new Set(arr)].sort(); }
function escapeHtml(s){ return String(s).replace(/[&<>"']/g, m => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#039;'}[m])); }
function sourceLinks(keys){
  return keys.filter(k=>SOURCES[k]).slice(0,4).map(k=>`<a href="${SOURCES[k].url}" target="_blank" title="${escapeHtml(SOURCES[k].note)}">${k}</a>`).join(' · ');
}
function filtered(){
  const q = $('#search').value.trim().toLowerCase();
  const region = $('#regionFilter').value;
  const theme = $('#themeFilter').value;
  const vis = $('#visibilityFilter').value;
  return DATA.filter(d => (!region || d.region===region) && (!theme || d.theme===theme) && (!vis || d.ai_visibility===vis) && (!q || [d.ticker,d.company,d.country,d.theme,d.latent_ai_asset,d.thesis].join(' ').toLowerCase().includes(q)));
}
function fillFilters(){
  for(const t of unique(DATA.map(d=>d.theme))){ $('#themeFilter').insertAdjacentHTML('beforeend', `<option value="${escapeHtml(t)}">${escapeHtml(t)}</option>`); }
  for(const v of unique(DATA.map(d=>d.ai_visibility))){ $('#visibilityFilter').insertAdjacentHTML('beforeend', `<option value="${escapeHtml(v)}">${escapeHtml(v)}</option>`); }
}
